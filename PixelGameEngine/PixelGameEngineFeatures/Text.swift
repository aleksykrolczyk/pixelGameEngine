import AppKit
import Foundation
import SwiftUI

extension PixelGameEngine {
    func drawText(_ text: String, at p: Point) {
        drawText(text, x: p.x, y: p.y)
    }

    func drawText(_ text: String, x: Int, y: Int) {
        var (globX, globY) = (x, y)

        for character in text {
            guard let v = character.asciiValue else { continue }
            if v == 32 { // space
                globX += Font.charSize.x
                continue
            }
            if v == 10 { // newline
                globX = x - 1
                globY += Font.charSize.y + 1
                continue
            }

            let index = Int(v) - Int(Font.offset)
            if index < 0 || index > 127 {
                continue
            }

            let fy = index / Font.charsPerRow
            let fx = index - (fy * Font.charsPerRow) - 1
            
            for i in 0 ..< Font.charSize.x {
                for j in 0 ..< Font.charSize.y {
                    let pixel = Font.data[fy * Font.charSize.y + j][fx * Font.charSize.x + i]
                    if pixel == 1 {
                        drawPixel(at: Point(globX + i, globY + j), color: [1.0, 1.0, 1.0, 1.0])
                    }
                }
            }

            globX += (Font.charSize.x + 1)
        }
    }
}

